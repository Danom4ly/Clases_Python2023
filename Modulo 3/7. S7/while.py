# * Imprimir valores del 1 al 5

i = 0
while i < 5:
    print(i) #1
    i += 1 #aumentando el valor de "i" como iterador

j = 1
while j <= 5:
    print(j) #1
    j += 1 #aumentando el valor de "i" como iterador

while True:
    print("imprime hasta que")
    break #terminar el ciclo